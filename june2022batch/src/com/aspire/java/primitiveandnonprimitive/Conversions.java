package com.aspire.java.primitiveandnonprimitive;

public class Conversions {
 public static void main(String[] args) {
	//boolean to string
	 boolean b= true;
	 System.out.println("Boolean as aString:"+ Boolean.toString(b));
	 
	 //String to double
	 String a= "123.645214";
	 System.out.println("String to double:"+ Double.parseDouble(a));
	 
	 //double to string
	 double c= 34.126264565;
	 System.out.println("Double to String:"+ Double.toString(c));
	 
	 //sTRING TO LONG
	 String d= "7895455";
	 System.out.println("String to long:"+ Long.parseLong(d));
	 
	 
}
}
