public class area_perimeter_of_circle {
	public static void main(String[] args) {
		float r=10f;
		float pi=3.14f;
        float a=pi*(r*r);
        System.out.println("area of circle ="+a);
        float c=2*pi*r;
        System.out.println("perimeter of circle="+c);
        
	}

}
